-- Resize splits with ease
vim.keymap.set("n", "<C-W><C-Left>", "<C-w>>")
vim.keymap.set("n", "<C-W><C-Right>", "<C-w><")
vim.keymap.set("n", "<C-W><C-Up>", "<C-w>+")
vim.keymap.set("n", "<C-W><C-Down>", "<C-w>-")

-- Swap splits with ease
vim.keymap.set("n", "<C-W><S-Left>", "<C-w>H")
vim.keymap.set("n", "<C-W><S-Right>", "<C-w>L")
vim.keymap.set("n", "<C-W><S-Up>", "<C-w>K")
vim.keymap.set("n", "<C-W><S-Down>", "<C-w>J")
